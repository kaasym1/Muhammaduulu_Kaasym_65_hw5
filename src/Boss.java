public class Boss {
    private int health;
    private int damage;
    private String protectionTipe;

    public int getHealth() {
        return health;
    }

    public void setHealth(int health) {
        this.health = health;
    }

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public String getProtectionTipe() {
        return protectionTipe;
    }

    public void setProtectionTipe(String protectionTipe) {
        this.protectionTipe = protectionTipe;
    }
}
